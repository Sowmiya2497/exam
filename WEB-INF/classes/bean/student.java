package bean;
public class student
{
  String id;
  String name,email;
  int score;
  
  public void setId(String id)
  {
    this.id = id;
  }
  
  public void setName(String n)
  {
    name = n;
  }
  
  public void setEmail(String e)
  {
    email = e;
  }
  
  public void setScore(int s)
  {
    score = s;
  }
  
  
  
  public String getId()
  {
    return id;
  }
  
  public String getName()
  {
    return name;
  }
  
  public String getEmail()
  {
    return email;
  }
  
  public int getScore()
  {
    return score;
  }
  
 
  
  
}
  
  
  